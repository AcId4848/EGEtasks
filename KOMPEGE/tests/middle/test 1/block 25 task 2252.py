from math import prod

for n in range(200000061, 1000000000):
    de = [d for d in range(2, 1000000) if n % d == 0]
    if len(de) < 5:
        de = [0]
    pn = prod(de[:5])
    if pn % 10 == 1 and n >= pn:
        print(pn, de[4])

